"""Nested list"""

def flatten(lst):

    flat_list = []
    for item in lst:
        if isinstance(item, list):
            flat_list.extend(item)  # add elements of sublist
        else:
            flat_list.append(item)  # add single element
    return flat_list

nested_list = [[1, 2], [3, 4], [5]]
result = flatten(nested_list)
print("Flattened List:", result)


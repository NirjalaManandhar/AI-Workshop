"""Sum of All Elements in a Nested List:"""

def sum_nested(lst):
    """
    Calculates the sum of all numbers in a nested list, including sublists at any depth.

    Parameters:
    lst (list): A nested list of numbers.

    Returns:
    int/float: Sum of all numbers in the nested list.
    """
    total = 0
    for item in lst:
        if isinstance(item, list):
            total += sum_nested(item)  # recursive call for sublist
        else:
            total += item  # add number directly
    return total

nested_list = [[1, 2], [3, [4, 5]], 6]
result = sum_nested(nested_list)
print("Sum of all elements:", result)

"""Remove Specific Element from a Nested List:"""

def remove_element(lst, elem):
    """
    Removes all occurrences of elem from a nested list.

    Parameters:
    lst (list): A nested list of elements.
    elem: The element to remove from the list.

    Returns:
    list: A new nested list with all occurrences of elem removed.
    """
    new_list = []
    for item in lst:
        if isinstance(item, list):
            # Recursively remove element from sublist
            new_list.append(remove_element(item, elem))
        else:
            if item != elem:
                new_list.append(item)
    return new_list

nested_list = [[1, 2], [3, 2], [4, 5]]
result = remove_element(nested_list, 2)
print("Modified List:", result)

"""Find the Maximum Element in a Nested List:"""

def find_max(lst):
    """
    Finds the maximum element in a nested list, regardless of depth.

    Parameters:
    lst (list): A nested list of numbers.

    Returns:
    int/float: The maximum number in the nested list.
    """
    max_value = float('-inf')  # start with smallest possible number
    for item in lst:
        if isinstance(item, list):
            # recursive call for sublist
            sub_max = find_max(item)
            if sub_max > max_value:
                max_value = sub_max
        else:
            if item > max_value:
                max_value = item
    return max_value

nested_list = [[1, 2], [3, [4, 5]], 6]
result = find_max(nested_list)
print("Maximum Element:", result)

"""Count Occurrences of an Element in a Nested List:"""

def count_occurrences(lst, elem):
    """
    Counts how many times elem appears in a nested list.

    Parameters:
    lst (list): A nested list of elements.
    elem: The element to count.

    Returns:
    int: Number of times elem appears in the nested list.
    """
    count = 0
    for item in lst:
        if isinstance(item, list):
            # Recursive call for sublist
            count += count_occurrences(item, elem)
        else:
            if item == elem:
                count += 1
    return count

nested_list = [[1, 2], [2, 3], [2, 4]]
result = count_occurrences(nested_list, 2)
print("Occurrences of 2:", result)

"""Flatten a List of Lists of Lists:"""

def deep_flatten(lst):
    """
    Flattens a deeply nested list into a single list, regardless of depth.

    Parameters:
    lst (list): A nested list of arbitrary depth.

    Returns:
    list: A new flattened list containing all elements.
    """
    flat_list = []
    for item in lst:
        if isinstance(item, list):
            # Recursively flatten the sublist
            flat_list.extend(deep_flatten(item))
        else:
            flat_list.append(item)
    return flat_list

nested_list = [[[1, 2], [3, 4]], [[5, 6], [7, 8]]]
result = deep_flatten(nested_list)
print("Flattened List:", result)

"""Nested List Average:"""

def average_nested(lst):
    """
    Calculates the average of all numbers in a nested list.

    Parameters:
    lst (list): A nested list of numbers.

    Returns:
    float: Average of all numbers in the nested list.
    """
    def flatten_and_count(lst):
        """
        Helper function that returns a tuple (total_sum, total_count)
        of all numbers in the nested list.
        """
        total_sum = 0
        total_count = 0
        for item in lst:
            if isinstance(item, list):
                sub_sum, sub_count = flatten_and_count(item)
                total_sum += sub_sum
                total_count += sub_count
            else:
                total_sum += item
                total_count += 1
        return total_sum, total_count

    total, count = flatten_and_count(lst)
    if count == 0:
        return 0  # avoid division by zero
    return total / count

nested_list = [[1, 2], [3, 4], [5, 6]]
result = average_nested(nested_list)
print("Average:", result)

